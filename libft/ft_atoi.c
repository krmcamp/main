int	ft_atoi(char *str)
{
	int minus;
	int i;
	int res;

	minus = 1;
	i = 0;
	res = 0;
	while (str[i] == '\t'
			|| str[i] == '\n' || str[i] == '\v'
			|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			minus = minus * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (res == 214748364 && (str[i] - '0') == 8)
			return (-2147483648);
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (res * minus);
}
